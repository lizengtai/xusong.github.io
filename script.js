// 游戏变量
let currentLevel = 1;
let usedSongs = [];
let currentSong = null;
let options = [];

// DOM 元素
const currentLevelEl = document.getElementById('current-level');
const difficultyEl = document.getElementById('difficulty');
const lyric1El = document.getElementById('lyric-1');
const lyric2El = document.getElementById('lyric-2');
const optionsEl = document.getElementById('options');
const resultEl = document.getElementById('result');
const restartBtn = document.getElementById('restart-btn');

// 初始化游戏
function initGame() {
    currentLevel = 1;
    usedSongs = [];
    updateLevelInfo();
    loadNextLevel();
    restartBtn.style.display = 'none';
}

// 更新关卡信息
function updateLevelInfo() {
    currentLevelEl.textContent = `第 ${currentLevel} 关 / 共 100 关`;
    
    // 计算难度
    const difficulty = Math.ceil(currentLevel / 5);
    let difficultyText = '简单';
    if (difficulty === 2) difficultyText = '中等';
    else if (difficulty === 3) difficultyText = '困难';
    else if (difficulty === 4) difficultyText = '专家';
    else if (difficulty >= 5) difficultyText = '大师';
    
    difficultyEl.textContent = `难度: ${difficultyText}`;
}

// 加载下一关
function loadNextLevel() {
    if (currentLevel > 100) {
        showVictory();
        return;
    }
    
    // 切换古风风格
    const styles = ['style-1', 'style-2', 'style-3', 'style-4'];
    const randomStyle = styles[Math.floor(Math.random() * styles.length)];
    
    // 移除所有风格类，添加新风格类
    document.body.className = '';
    document.body.classList.add(randomStyle);
    
    // 随机选择未使用的歌曲
    let availableSongs = songs.filter(song => !usedSongs.includes(song.title));
    if (availableSongs.length === 0) {
        // 如果所有歌曲都用完了，重新开始使用
        usedSongs = [];
        availableSongs = songs;
    }
    
    // 根据难度选择歌曲
    const difficulty = Math.ceil(currentLevel / 5);
    let filteredSongs;
    if (difficulty === 1) {
        // 简单难度：前50首热门歌曲
        filteredSongs = availableSongs.filter(song => songs.indexOf(song) < 50);
    } else if (difficulty === 2) {
        // 中等难度：51-100首专辑歌曲
        filteredSongs = availableSongs.filter(song => songs.indexOf(song) >= 50 && songs.indexOf(song) < 100);
    } else if (difficulty === 3) {
        // 困难难度：101-150首深度歌曲
        filteredSongs = availableSongs.filter(song => songs.indexOf(song) >= 100 && songs.indexOf(song) < 150);
    } else if (difficulty === 4) {
        // 专家难度：151-200首冷门歌曲
        filteredSongs = availableSongs.filter(song => songs.indexOf(song) >= 150 && songs.indexOf(song) < 200);
    } else {
        // 大师难度：201-224首早期歌曲
        filteredSongs = availableSongs.filter(song => songs.indexOf(song) >= 200);
    }
    
    // 如果没有符合难度的歌曲，重新使用所有歌曲并按难度筛选
    if (filteredSongs.length === 0) {
        usedSongs = [];
        availableSongs = songs;
        if (difficulty === 1) {
            filteredSongs = availableSongs.filter(song => songs.indexOf(song) < 50);
        } else if (difficulty === 2) {
            filteredSongs = availableSongs.filter(song => songs.indexOf(song) >= 50 && songs.indexOf(song) < 100);
        } else if (difficulty === 3) {
            filteredSongs = availableSongs.filter(song => songs.indexOf(song) >= 100 && songs.indexOf(song) < 150);
        } else if (difficulty === 4) {
            filteredSongs = availableSongs.filter(song => songs.indexOf(song) >= 150 && songs.indexOf(song) < 200);
        } else {
            filteredSongs = availableSongs.filter(song => songs.indexOf(song) >= 200);
        }
    }
    
    const randomIndex = Math.floor(Math.random() * filteredSongs.length);
    currentSong = filteredSongs[randomIndex];
    usedSongs.push(currentSong.title);
    
    // 生成连续的两句歌词
    const lyrics = currentSong.lyrics;
    if (lyrics.length < 2) {
        lyric1El.textContent = lyrics[0];
        lyric2El.textContent = lyrics[0];
    } else {
        const startIndex = Math.floor(Math.random() * (lyrics.length - 1));
        lyric1El.textContent = lyrics[startIndex];
        lyric2El.textContent = lyrics[startIndex + 1];
    }
    
    // 生成选项
    generateOptions();
    resultEl.textContent = '';
}

// 生成选项
function generateOptions() {
    options = [currentSong.title];
    
    // 添加三个错误选项
    while (options.length < 4) {
        const randomSong = songs[Math.floor(Math.random() * songs.length)];
        if (!options.includes(randomSong.title)) {
            options.push(randomSong.title);
        }
    }
    
    // 打乱选项顺序
    options.sort(() => Math.random() - 0.5);
    
    // 渲染选项
    optionsEl.innerHTML = '';
    options.forEach(option => {
        const button = document.createElement('button');
        button.className = 'option-btn';
        button.textContent = option;
        button.addEventListener('click', () => checkAnswer(option, button));
        optionsEl.appendChild(button);
    });
}

